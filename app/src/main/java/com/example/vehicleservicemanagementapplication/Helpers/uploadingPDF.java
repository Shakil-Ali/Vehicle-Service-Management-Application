package com.example.vehicleservicemanagementapplication.Helpers;

// https://www.youtube.com/watch?v=lmJHtSChZG0

// Uploading PDF Class
public class uploadingPDF
{
    // Initialise variables
    public String name;
    public String url;

    // Empty constructor (base)
    public uploadingPDF()
    {

    // end of constructor
    }

    public uploadingPDF(String name, String url)
    {
        this.name = name;
        this.url = url;

    // end of two parameter constructor
    }


    // GETTERS
    public String getName()
    {
        return name;
    }

    public String getUrl()
    {
        return url;
    }





// end of class
}
