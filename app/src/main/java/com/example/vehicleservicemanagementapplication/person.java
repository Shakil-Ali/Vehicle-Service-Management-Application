// Public class Person - for the users
public class person
{
    // String variable initialisations
    public String fullName, Username, Email;

    // Basic Constructor
    public person()
    {

    }

    // Three Variable Constructor
    public person(String fullName, String username, String email)
    {
        this.fullName = fullName;
        Username = username;
        Email = email;
    }
}
